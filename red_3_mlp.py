# Objetivo: helpers (clamp, softmax, JS, ECE).

def clamp01(x: float) -> float:
    return float(max(0.0, min(1.0, x)))

def softmax_np(x: np.ndarray) -> np.ndarray:
    x = x - np.max(x)
    e = np.exp(x)
    return e / np.sum(e)

def jensen_shannon(p: np.ndarray, q: np.ndarray, eps: float = 1e-12) -> float:
    p = np.clip(p, eps, 1.0)
    q = np.clip(q, eps, 1.0)
    p = p / p.sum()
    q = q / q.sum()
    m = 0.5 * (p + q)
    kl_pm = np.sum(p * np.log(p / m))
    kl_qm = np.sum(q * np.log(q / m))
    return float(0.5 * (kl_pm + kl_qm))

def expected_calibration_error(probs: np.ndarray, y_true: np.ndarray, n_bins: int = 12) -> float:
    conf = probs.max(axis=1)
    pred = probs.argmax(axis=1)
    acc = (pred == y_true).astype(np.float32)

    bins = np.linspace(0.0, 1.0, n_bins + 1)
    ece = 0.0
    for i in range(n_bins):
        lo, hi = bins[i], bins[i+1]
        mask = (conf >= lo) & (conf < hi)
        if mask.sum() == 0:
            continue
        bin_acc = float(acc[mask].mean())
        bin_conf = float(conf[mask].mean())
        ece += (mask.sum() / len(conf)) * abs(bin_acc - bin_conf)
    return float(ece)

