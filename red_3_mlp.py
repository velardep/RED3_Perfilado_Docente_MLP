def bootstrap_scores(row: pd.Series) -> Dict[str, float]:
    S_practico = 0.6*row["pdc_practice_ratio_30d"] + 0.4*row["chat_intent_practice_ratio_30d"]
    S_magistral = 0.6*row["pdc_theory_ratio_30d"] + 0.4*row["chat_intent_explain_ratio_30d"]
    S_evaluativo = 0.5*row["pdc_has_eval_ratio_30d"] + 0.3*row["pdc_has_rubric_ratio_30d"] + 0.2*row["chat_intent_eval_ratio_30d"]
    S_tecnologico = 0.7*row["resource_tech_ratio_30d"] + 0.3*min(1.0, row["resources_suggested_30d"]/10.0)
    S_reflexivo = 0.8*row["pdc_iterative_index_30d"] + 0.2*min(1.0, row["pdc_uploads_30d"]/5.0)

    # señal humana: fricción alta tiende a "iterativo" / "evaluativo"; aprobación alta tiende a cerrar rápido (más estructurado)
    S_reflexivo = clamp01(S_reflexivo + 0.15*row["friction_ratio_30d"])
    S_evaluativo = clamp01(S_evaluativo + 0.10*row["friction_ratio_30d"])
    S_magistral = clamp01(S_magistral + 0.10*row["approval_ratio_30d"])

    return {
        "PRACTICO_APLICADO": float(clamp01(S_practico)),
        "MAGISTRAL_ESTRUCTURADO": float(clamp01(S_magistral)),
        "EVALUATIVO": float(clamp01(S_evaluativo)),
        "TECNOLOGICO_INNOVADOR": float(clamp01(S_tecnologico)),
        "REFLEXIVO_ITERATIVO": float(clamp01(S_reflexivo)),
    }

def pick_style_with_fallback(row: pd.Series, scores: Dict[str, float]) -> str:
    items = sorted(scores.items(), key=lambda kv: kv[1], reverse=True)
    top1, top2 = items[0], items[1]
    margin = top1[1] - top2[1]

    if margin >= MARGIN_THRESHOLD:
        return top1[0]

    # fallback: si fricción alta -> reflexivo; si tech alto -> tecnológico; si pdc eval alto -> evaluativo
    if row["friction_ratio_30d"] >= 0.45:
        return "REFLEXIVO_ITERATIVO"
    if row["resource_tech_ratio_30d"] >= 0.55:
        return "TECNOLOGICO_INNOVADOR"
    if row["pdc_has_eval_ratio_30d"] >= 0.55 or row["chat_intent_eval_ratio_30d"] >= 0.55:
        return "EVALUATIVO"

    return top1[0]

