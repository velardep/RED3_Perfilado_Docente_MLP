def simulate_time_series(base: Dict[str, float], steps: int = 10, noise: float = 0.06) -> List[Dict[str, float]]:
    series = []
    cur = base.copy()
    for _ in range(steps):
        nxt = cur.copy()
        for k in FEATURE_NAMES:
            v = float(nxt[k])
            if FEATURE_TYPES[k] == "float":
                if k == "chat_avg_len_30d":
                    v = max(20.0, v + float(np.random.normal(0, 25.0)))
                else:
                    v = clamp01(v + float(np.random.normal(0, noise)))
            else:
                v = max(0, int(round(v + float(np.random.normal(0, max(1.0, noise*12))))))
            nxt[k] = v
        series.append(nxt)
        cur = nxt
    return series

